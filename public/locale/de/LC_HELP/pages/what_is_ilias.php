<h1><font size="+1">Was ist ILIAS?<br>
</font></h1>
Bei ILIAS handelt es sich nicht um einen Teil von Stud.IP, sondern um
eine eigenst&auml;ndige Arbeitsumgebung, mit der unter anderem
Lerneinheiten und Kurse erstellt, verwaltet und genutzt werden
k&ouml;nnen. Die Software wurde im Rahmen des Projekts VIRTUS an der
Wirtschafts- und Sozialwissenschaftlichen Fakukult&auml;t der Uni
K&ouml;ln entwickelt.<br>
Wir haben Stud.IP mit ILIAS verbunden, so dass innerhalb von Stud.IP
Veranstaltungen in ILIAS erstellte Lernmodule genutzt werden k&ouml;nnen
und man beide System ohne eine erneute Anmeldung nutzen kann. So dient
Stud.IP der Kommunikation, der Verwaltung von
Pr&auml;senzveranstaltungen und Planung des Uni-Alltags f&uuml;r
Lernende und Dozierende und andere Mitarbeiter, w&auml;hrend ILIAS
Lernmodule, Online-Kurse und Tests bietet, die f&uuml;r Nutzer von
Stud.IP so direkt zug&auml;nglich gemacht werden k&ouml;nnen.<br>
<br>
Es gilt vor allem: <span style="font-style: italic;">Keine Panik, wenn
bei dem Klick auf ein Ilias Lernmodul pl&ouml;tzlich ein ganz anderes
System erscheint...</span>.<br>
<br>
N&auml;here Informationen zu ILIAS auf der <a
 href="http://www.ilias.uni-koeln.de" target="new">Portalseite des
Projektes</a>.<br>
<br>
<h1><font size="+1">Wie kann ich denn nun so ein Lernmodul nutzen?</font></h1>
<p> Bevor man zum ersten Mal ein Lernmodul aus ILIAS nutzen kann, muss
ein bestehender Account (es spielt keine Rolle, ob man zuvor bereits in
Stud.IP angemeldet war, oder in ILIAS) verkn&uuml;pft werden. Falls noch
kein Account verkn&uuml;pft wurde, fragt Stud.IP automatisch nach und
bietet den entsprechenden Link auf die Verkn&uuml;pfungsfunktion.
Klicken Sie einfach auf "Mein ILIAS Account". Nun gibt es zwei
M&ouml;glichkeiten zur Auswahl: <br>
</p>
<ul>
  <li>Haben sie schon einen Account im ILIAS System an ihrer
Einrichtung, dann k&ouml;nnen sie diesen bestehen Account mit ihrem
Stud.IP Account verbinden, so dass keinerlei Einstellungen des alten
Accounts verloren gehen. <br>
  </li>
  <li>Haben sie keinen Account im entsprechenden ILIAS System, so
k&ouml;nnen Sie ihren Stud.IP Account auch in ILIAS anlegen. Dabei
werden die Rechtestufen entprechend gesetzt.</li>
</ul>
Wenn das erledigt ist, k&ouml;nnen Sie in allen Veranstaltungen, die
mit ILIAS Lernmodulen verbunden sind, direkt &uuml;ber den
entsprechenden Link in das ILIAS System einsteigen. <br>
<br>
<span style="font-style: italic;">Achtung: Wenn sie ein Lernmodul
starten, &ouml;ffnet sich ILIAS in einem neuen Fenster! Dieses Fenster
k&ouml;nnen Sie am Ende schlie&szlig;en, ihr Stud.IP Fenster bleibt
weiter erhalten.<br>
</span><br>
