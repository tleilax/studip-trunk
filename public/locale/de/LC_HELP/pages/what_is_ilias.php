<h1><font size="+1">Eigene Lernmodule erstellen</font></h1>
Stud.IP kann so eingerichtet werden, dass jeder Nutzer eigene
Lernmodule erstellen kann. Wenn diese M&ouml;glichkeit gegeben ist,
erscheint auf der &Uuml;bersicht von verf&uuml;gbaren Lernmodulen ein
Link, mit dem Sie in das ILIAS System einspringen k&ouml;nnen, um dort
Lernmodule anzulegen. Einzelheiten finden sie in der <a
 href="<?=$ABSOLUTE_PATH_ILIAS?>help/de/editor/index.html">Hilfe
des IILIAS-Systems</a>.<br>
Wenn Sie ein Lernmodul erstellt haben, fragen Sie den Dozenten oder
Tutor ihrer Veranstaltung, falls Sie das Lernmodul auch anderen Nutzern
innerhalb der Veranstaltung zur Verf&uuml;gung stellen wollen.<br>
